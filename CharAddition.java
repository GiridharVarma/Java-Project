package com.te.learnJava8.logics;

public class CharAddition {
    public static void main(String[] args) {
        char char1 = 'A'; // ASCII value of 'A' is 65
        char char2 = 'B'; // ASCII value of 'B' is 66

        int result = char1 + char2;

        System.out.println("The result of adding '" + char1 + "' and '" + char2 + "' is: " + result);
        
        System.out.println();
        
    }
}

